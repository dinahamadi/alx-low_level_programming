#include "3-calc.h"

/**
 * op_add - check code
 * @a: an integer
 * @b: an integer
 * Return: sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - check code
 * @a: an integer
 * @b: an integer
 * Return: substract b from a.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - check code
 * @a: an integer
 * @b: an integer
 * Return: multiply a by b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - check code
 * @a: an integer
 * @b: an integer
 * Return: divide a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - check code
 * @a: an integer
 * @b: an integer
 * Return: the reminde of a div b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
